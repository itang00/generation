# load doc into memory
def load_doc(filename):
	file = open(filename, 'r', encoding='utf-8')
	text = file.read()
	file.close()
	return text.lower() # lower case letters only

# load the text file
raw_text = load_doc('shakespeare.txt')

# map letters to numbers and numbers to letters
chars = sorted(list(set(raw_text)))
char_to_int = dict((c, i) for i, c in enumerate(chars))
int_to_char = dict((i, c) for i, c in enumerate(chars))

# set sequence length to 40
# X is input with len 40 sequences
# Y is next letter in text file
seq_length = 40
X = []
Y = []

# iterate text file with step size 3
for i in range(0, len(raw_text) - seq_length, 3):
	X.append([char_to_int[char] for char in raw_text[i:i + seq_length]])
	Y.append(char_to_int[raw_text[i + seq_length]])
 
# one hot encode input variables
sequences = [to_categorical(x, len(char_to_int)) for x in X]
X = np.array(sequences)

# one hot encode the output variable
y = np_utils.to_categorical(Y)

# model with 200 units

model = Sequential()
model.add(LSTM(200,input_shape=(X.shape[1], X.shape[2])))
model.add(Dense(y.shape[1],activation='softmax'))

model.compile(loss='categorical_crossentropy',optimizer ='adam',metrics=['accuracy'])

